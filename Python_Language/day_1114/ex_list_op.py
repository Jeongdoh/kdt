# --------------------------------------
# 리스트 자료형의 연산 => 덧셈, 곱셈
# - 덧셈 : 리스트 +리스트
# - 곱셈 : 리스트 * 정수
# --------------------------------------

# str 타입 보기-------------------------
msg1="Hello~"
msg2="2022"

print('+', msg1+msg2)
print('*', msg1*5)

# list 타입 보기 ----------------------
nums1=[1,3,5]
datas=['A','B','C']
etc=[False, 4.7, [11,22]]

print('+', nums1+datas)
print('+', nums1+etc)
print('+', nums1+nums1)

print('*', nums1*3)
print('*', nums1*10)