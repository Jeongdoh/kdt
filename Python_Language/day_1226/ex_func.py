# 기능 : 회원가입 함수 반환=> 아이디, 비밀번호, 이메일 ,전화번호  [ 지역, 직업, 나이....]
# 함수명 : join
# 매개변수 : id, pw, email, phone, *data
# 반환값 : 축하메세지
#----------------------------------------------------------------------
def join(id, pw, email, phone, **data):
    '''
    id int
    email str
    '''
    print(type(data))
    personInfo={}
    personInfo['id']=id
    personInfo['pw']=pw
    personInfo['email']=email
    personInfo['phone']=phone

    for k,v in data.items():
        personInfo[k]=v


    for k in data.keys():
        personInfo[k]=data[k]

    return personInfo

# 함수 호출 =>(매개변수명=값)
#z키워드 파라미터가 있는 함수 => 호출 -> 키=값!!! 키는 str타입만 가능
member=join('a123','s1111','a@naver.com','010-1234-5678',job='student',age=12)

print(f'member -> {member}')
