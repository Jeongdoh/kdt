# 기능 : 2개 이상 데이터 덧셈 후 결과 반환
# 함수명 : addMore
# 매개변수 : a b *nums
# 반환값 : 덧셈결과
#----------------------------------------------------------------------
#Default value 기본값 - 함수 호출시 인자를 주지 않는 경우
# 사용되는 값
def addMore(a,b, *nums):
    result=a+b
    for num in nums:
        result+=num
    return result

# 함수 사용 : 함수 호출 : 함수명(인자)=======
# 기본값 매개변수가 여러개인 경우는 값 지정시 매개변수명=값
print(addMore(10,5), addMore(1,2,3,4,5))

